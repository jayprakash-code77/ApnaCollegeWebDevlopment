// 1. Arithmetic Operators
//    - `+` (Addition): Adds two numbers.
//    - `-` (Subtraction): Subtracts one number from another.
//    - `*` (Multiplication): Multiplies two numbers.
//    - `/` (Division): Divides one number by another.
//    - `%` (Modulus): Returns the remainder of division.
//    - `**` (Exponentiation): Raises the first number to the power of the second.
//    - `++` (Increment): Increases an integer by one.
//    - `--` (Decrement): Decreases an integer by one.
//
// 2. Assignment Operators
//    - `=` (Assignment): Assigns a value to a variable.
//    - `+=` (Addition Assignment): Adds and assigns.
//    - `-=` (Subtraction Assignment): Subtracts and assigns.
//    - `*=` (Multiplication Assignment): Multiplies and assigns.
//    - `/=` (Division Assignment): Divides and assigns.
//    - `%=` (Modulus Assignment): Finds remainder and assigns.
//    - `**=` (Exponentiation Assignment): Raises to power and assigns.
//
// 3. Comparison Operators
//    - `==` (Equal to): Checks if values are equal.
//    - `===` (Strict Equal to): Checks if values and types are equal.
//    - `!=` (Not Equal to): Checks if values are not equal.
//    - `!==` (Strict Not Equal to): Checks if values and types are not equal.
//    - `>` (Greater than): Checks if the left value is greater.
//    - `<` (Less than): Checks if the left value is smaller.
//    - `>=` (Greater than or Equal to): Checks if left value is greater or equal.
//    - `<=` (Less than or Equal to): Checks if left value is smaller or equal.
//
// 4. Logical Operators
//    - `&&` (AND): Returns true if both operands are true.
//    - `||` (OR): Returns true if one of the operands is true.
//    - `!` (NOT): Reverses the boolean state.
//
// 5. Bitwise Operators
//    - `&` (AND): Performs bitwise AND.
//    - `|` (OR): Performs bitwise OR.
//    - `^` (XOR): Performs bitwise XOR.
//    - `~` (NOT): Performs bitwise NOT.
//    - `<<` (Left Shift): Shifts bits to the left.
//    - `>>` (Right Shift): Shifts bits to the right.
//    - `>>>` (Unsigned Right Shift): Shifts bits to the right with zero-fill.
//
// 6. String Operators
//    - `+` (Concatenation): Combines strings.
//    - `+=` (Concatenation Assignment): Adds to the end of a string.
//
// 7. Ternary Operator
//    - `? :` (Conditional): Returns one of two values based on a condition.
//
// 8. Type Operators
//    - `typeof`: Returns the data type.
//    - `instanceof`: Checks if an object is an instance of a class.
//
// 9. Unary Operators
//    - `+` (Unary Plus): Converts operand to number.
//    - `-` (Unary Negation): Converts operand to negative.
//    - `++` (Increment): Increases by one.
//    - `--` (Decrement): Decreases by one.
//    - `!` (Logical NOT): Inverts a boolean.
//
// 10. Comma Operator
//    - `,` (Comma): Evaluates multiple expressions and returns the last one.
//
// 11. Optional Chaining
//    - `?.`: Allows access to nested properties without errors if intermediate properties donâ€™t exist.
//
// 12. Nullish Coalescing
//    - `??`: Returns the right operand if the left operand is `null` or `undefined`.






